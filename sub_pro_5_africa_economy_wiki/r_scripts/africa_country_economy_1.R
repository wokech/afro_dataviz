# (E) Plots

## 1) A bar plot demonstrating the GDP of the top 5 economies relative to the rest of the continent

ggplot(africa_country_GDP_clean_1, aes(reorder(country, +nominal_gdp), nominal_gdp, fill = country)) +
  geom_bar(stat = "identity") + coord_flip() +
  gghighlight(max(nominal_gdp) > 200) + 
  scale_fill_brewer(palette="GnBu") +
  labs(x = "Country",
       y = "GDP (billions of US$)",
       title = "Majority of Africa's nominal GDP in 2024\nwas generated by 5 countries",
       subtitle = "",
       caption = "Data Source: Wikipedia") +
  theme_classic() +
  scale_y_continuous(labels = comma,
                     breaks = seq(0, 450, by = 100),
                     minor_breaks = seq(0, 450, by = 10)) +
  theme(axis.title.x =element_text(size = 25),
        axis.title.y =element_text(size = 25),
        axis.text.x = element_text(size = 25),
        axis.text.y = element_text(size = 15),
        axis.ticks.length.x = unit(0.2, "cm"),  # Lengthen the ticks
        axis.ticks.minor.x = element_line(color = "black", size = 2),  # Show minor ticks
        plot.title = element_text(family="Helvetica", face="bold", size = 35, hjust = 0.5, vjust=0),
        plot.subtitle = element_text(family="Helvetica", face="bold", size = 25),
        plot.caption = element_text(family = "Helvetica",size = 25, face = "bold", hjust = -0.75, vjust = 1),
        plot.background = element_rect(fill = "bisque1", colour = "bisque1"),
        panel.background = element_rect(fill = "bisque1", colour = "bisque1"),
        plot.title.position = 'plot',
        legend.title = element_blank(),
        legend.position = "none") 

ggsave("sub_pro_5_africa_economy_wiki/images/bar_top_5_africa_gdp_2024.png", width = 12, height = 12, dpi = 300)

## 2) A tree map demonstrating the GDP of the top 5 economies relative to the rest of the continent

# Get the top 5 economies
africa_country_GDP_clean_top_5 <- africa_country_GDP_clean_1[1:5, ]

# Get the bottom 49
africa_country_GDP_clean_bottom <- africa_country_GDP_clean_1[6:54, ]

# Sum the bottom 49
africa_country_GDP_clean_bottom_total <- africa_country_GDP_clean_bottom %>%
  bind_rows(summarise(., across(where(is.numeric), sum),
                      across(where(is.character), ~'Rest of the Continent'))) %>%
  filter(row_number() %in% 50)

# Combine the top 5 economies with the combined GDP of the other 49 countries
africa_country_GDP_clean_top_5_bottom_total <- rbind(africa_country_GDP_clean_top_5, 
                                                     africa_country_GDP_clean_bottom_total)

# Create new columns to provide labels for the treemap
africa_country_GDP_clean_top_5_bottom_total_new_col <- africa_country_GDP_clean_top_5_bottom_total %>%
  mutate(nominal_gdp_new_1 = paste("$", nominal_gdp, sep = "")) %>%
  mutate(nominal_gdp_new_2 = paste(nominal_gdp_new_1, "Billion", sep = " ")) 

# Plot the tree map
ggplot(africa_country_GDP_clean_top_5_bottom_total_new_col, 
       aes(area = nominal_gdp, fill = country, 
           label = paste(country, nominal_gdp_new_2, sep = "\n"))) +
  geom_treemap() +
  labs(title = "Over half of Africa's nominal GDP is\ngenerated by 5 countries",
       subtitle = "",
       fill = "",
       caption = "Data Source: Wikipedia") +
  geom_treemap_text(colour = "black",
                    place = "centre",
                    size = 25,
                    grow = FALSE) + 
  theme(legend.position = "none",
        plot.title = element_text(size=40, hjust = 0.5, vjust = 1, face="bold"),
        plot.subtitle = element_text(size=15, hjust = 0.5, vjust = 1),
        legend.text = element_text(size = 20),
        plot.caption = element_text(size = 25, hjust = 0, vjust = 1, face = "bold"),
        panel.background = element_rect(fill="bisque1"),
        plot.background = element_rect(fill="bisque1"),
        legend.background = element_rect(fill="bisque1")) +
  scale_fill_brewer(palette = "GnBu")

ggsave("sub_pro_5_africa_economy_wiki/images/treemap_top_5_africa_gdp_2024.png", width = 12, height = 12, dpi = 300)

